import 'package:flutter/material.dart';

import 'dashboardscreen.dart';

class OnboardScreen extends StatelessWidget {
  const OnboardScreen({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: PageView(
        children: [
          _buildOnboardPage(
            context,
            image: "lib/assets/image/a.jpg",
            title: 'Discover New designs',
            description: 'Find the best tatto crafting.',
          ),
          _buildOnboardPage(
            context,
            image: 'assets/onboard2.png',
            title: 'Book Easily',
            description: 'Simply tap for appoinment.',
          ),
          _buildOnboardPage(
            context,
            image: 'assets/onboard3.png',
            title: 'Make it count',
            description: 'Experience comfortable and unforgettable moments.',
            isLastPage: true,
          ),
        ],
      ),
    );
  }

  Widget _buildOnboardPage(
    BuildContext context, {
    required String image,
    required String title,
    required String description,
    bool isLastPage = false,
  }) {
    return Padding(
      padding: const EdgeInsets.all(20.0),
      child: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        crossAxisAlignment: CrossAxisAlignment.center,
        children: [
          Image.asset(image, height: 300),
          const SizedBox(height: 40),
          Text(
            title,
            style: const TextStyle(
              fontSize: 28,
              fontWeight: FontWeight.bold,
              color: Colors.indigo,
            ),
            textAlign: TextAlign.center,
          ),
          const SizedBox(height: 20),
          Text(
            description,
            style: const TextStyle(
              fontSize: 16,
              color: Colors.indigoAccent,
            ),
            textAlign: TextAlign.center,
          ),
          const SizedBox(height: 40),
          if (isLastPage)
            ElevatedButton(
              onPressed: () {
                Navigator.pushReplacement(
                  context,
                  MaterialPageRoute(
                    builder: (context) => const RegisterScreen(),
                  ),
                );
              },
              style: ElevatedButton.styleFrom(
                backgroundColor: Colors.indigo,
                foregroundColor: Colors.white,
                minimumSize: const Size(double.infinity, 50),
                shape: RoundedRectangleBorder(
                  borderRadius: BorderRadius.circular(10.0),
                ),
              ),
              child: const Text(
                "Get Started",
                style: TextStyle(fontSize: 18),
              ),
            ),
        ],
      ),
    );
  }
}

